    private void Resultadoboton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Resultadoboton2ActionPerformed
        // TODO add your handling code here:
        double vely, angy, tiempoy,gravy,sen;
        double b=0;
        double cambio =2;
        double resultado2= Math.sqrt(cambio);
        
        vely = Double.parseDouble(textvely.getText());
        angy = Double.parseDouble(textAngy.getText());
        gravy = Double.parseDouble(textGravy.getText());
        tiempoy = Double.parseDouble(textTiempoy.getText());
        
        
        b = Math.toRadians(angy);
        sen = Math.sin(b);
        
        resultado2 = vely*sen*tiempoy - 0.5*(gravy)*tiempoy*tiempoy;
        
        textResultado2.setText(String.format("%.2f",resultado2));
    }//GEN-LAST:event_Resultadoboton2ActionPerformed

