    private void Resultadoboton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Resultadoboton3ActionPerformed
        // TODO add your handling code here:
        double velmru, angmru,sen, tiempomru,posicionmru;
        double b=0;
        double cambio =2;
       double resultado3= Math.sqrt(cambio);
       
        velmru = Double.parseDouble(textVelomru.getText());
        angmru = Double.parseDouble(textAngmru.getText());
        tiempomru = Double.parseDouble(textTiempomru.getText());
        tiempomru = Double.parseDouble(textTiempomru.getText());
        posicionmru = Double.parseDouble(textPosicionmru.getText());
        
         b = Math.toRadians(angmru);
        sen = Math.cos(b);
        
        resultado3 = posicionmru + velmru*sen*tiempomru;
        
        textResultado3.setText(String.format("%.2f",resultado3));
    }//GEN-LAST:event_Resultadoboton3ActionPerformed

